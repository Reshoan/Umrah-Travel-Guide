                        <div class="text-left mx-auto mb-5 wow fadeInUp" data-wow-delay="0.1s" style="max-width: 600px;">
                            <!--<img src="img/step2.png" alt="Step 1" class="img-fluid mb-3">-->
                            <h5>Step 8: Sa’ee:</h5>
                            <p>Sa'ee literally means to walk and to run. In Shariah, it refers to covering the distance between Safa and Marwah seven times.</p>

                            <h6>Key Rules of Sa'ee</h6>
                            <ul>
                                <li>Niyyah (intention) is not a prerequisite for Sa'ee.</li>
                                <li>Sa'ee must be performed after a Tawaf.</li>
                                <li>Wudhu is not a requirement for Sa'ee.</li>
                                <li>Women can perform Sa'ee during their period.</li>
                                <li>Sa'ee must be performed on foot unless there is a valid reason.</li>
                                <li>It is Makrooh to indulge in vain talk during Sa'ee.</li>
                                <li>It is Makrooh to take intervals between the seven rounds without a valid excuse.</li>
                            </ul>
                        
                            <h6>Istilam of Hajr-e-Aswad</h6>
                            <p>Prior to performing Sa'ee, it is Sunnah to perform Istilam. You will be performing Istilam for the ninth time, following the eight times during Tawaf.</p>
                            <p>This Istilam is applicable if you are going to perform Sa'ee immediately after Tawaf.</p>
                        
                            <h6>Start of Sa'ee</h6>
                            <ul>
                                <li>Proceed towards the Mount of Safa, reciting the following:</li>
                            </ul>
                            <p style="direction: rtl; font-size: 22px; font-weight: bold; text-align: center;">
                                إِنَّ الصَّفَا وَالْمَرْوَةَ مِنْ شَعَائِرِ الله ❁ ۖ فَمَنْ حَجَّ الْبَيْتَ أَوِ اعْتَمَرَ فَلَا جُنَاحَ عَلَيْهِ أَن يَطَّوَّفَ بِهِمَا ۚ وَمَن تَطَوَّعَ خَيْرًا فَإِنَّ اللهَ شَاكِرٌ عَلِيمٌ ❁
                            </p>
                        
                            <blockquote>
                                <p><strong>Translation:</strong> "Indeed, Safa and Marwa are among the symbols of Allah. So whoever makes Hajj to the House or performs Umrah – there is no blame upon him for walking between them. And whoever volunteers good – then indeed, Allah is appreciative and Knowing."</p>
                        
                            <ul>
                                <li>Make the intention to perform Sa'ee (not a prerequisite).</li>
                                <li>Climb the hill of Safa, face the Kaabah, raise your hands in dua and recite.</li>
                            </ul>
                                <p style="direction: rtl; font-size: 22px; font-weight: bold; text-align: center;">
                                    اللّٰهُ أَكْبَرُ ❁ اللّٰهُ أَكْبَرُ ❁ اللّٰهُ أَكْبَرُ ❁ وَلِلّٰهِ الْحَمْدُ ❁
                                </p>

                                <blockquote>
                                    <strong>Transliteration:</strong> Allāhu akbar, Allāhu akbar, Allāhu akbar, wa li Llāhi l-ḥamd.
                                    <br>
                                    <strong>Translation:</strong> "Allah is the Greatest; Allah is the Greatest; Allah is the Greatest, and to Allah belongs all praise."
                                </blockquote>

                                <p style="direction: rtl; font-size: 22px; font-weight: bold; text-align: center;">
                                    لَآ إِلٰهَ إِلَّا اللّٰهُ وَحْدَهُ لاَ شَرِيكَ لَهُ ❁ لَهُ الْمُلْكُ وَلَهُ الْحَمْدُ ❁ يُحْيِي وَيُمِيتُ ❁ وَهُوَ عَلَى كُلِّ شَيْءٍ قَدِيرٌ ❁
                                </p>

                                <blockquote>
                                    <strong>Transliteration:</strong> La ilāha illa Llāhu waḥdahu la sharīka lah, lahu l-mulku wa lahu l-ḥamd, yuḥīī wa yumīt, wa huwa ʿalā kulli shay’in qadīr.
                                    <br>
                                    <strong>Translation:</strong> "There is no deity except Allah, alone without a partner. To Him belongs the Dominion, and to Him belongs all praise. He gives life and death, and He has power over everything."
                                </blockquote>

                                <p style="direction: rtl; font-size: 22px; font-weight: bold; text-align: center;">
                                    لَآ إِلٰهَ إِلَّا اللّٰهُ وَحْدَهُ ❁ اَنْجَزَ وَعْدَهُ وَنَصَرَ عَبْدَهُ وَهَزَمَ اَلْأَحْزَابَ وَحْدَهُ ❁
                                </p>

                                <blockquote>
                                    <strong>Transliteration:</strong> La ilāha illa Llāhu waḥdah, anjaza waʿdahu wa naṣara ʿabdahu wa hazama l-aḥzāba waḥdah.
                                    <br>
                                    <strong>Translation:</strong> "There is no deity except Allah alone. He fulfilled His promise, supported His slave, and defeated the Confederates alone."
                                </blockquote>
                        
                            <h6>Additional Recommended Supplication</h6>
                            
                            <p style="direction: rtl; font-size: 20px; font-weight: bold; text-align: center;">
                                رَبِّ اغْفِرْ وَارْحَمْ وَأَنْتَ الأَعَزُّ الأَكْرَمُ
                            </p>
                            
                            <blockquote> "Rabbighfir warham wa antal a’azul akram" </blockquote>
                            
                            <p><strong>Translation:</strong> "Oh Allah, forgive me and have mercy upon me, for You are the Most Mighty and Honourable."</p>
                        
                            <h6>Proceeding Towards Marwah</h6>
                            <ul>
                                <li>Climb down from Safa and start walking towards Marwah.</li>
                                <li>You may make supplications in any language, engage in Dhikr, or recite Durud. There is no prescribed supplication during Sa'ee.</li>
                                <li>Men must jog or walk briskly between the green lights.</li>
                            </ul>
                        
                            <h6>At Marwah</h6>
                            <ul>
                                <li>When you reach the top of Marwah, praise Allah facing the Kaabah.</li>
                                <li>Repeat the same words that were recited at Safa.</li>
                                <li>One trip from Safa to Marwah is considered one round.</li>
                                <li>Seven such rounds must be completed.</li>
                            </ul>
                        
                            <h6>End of Sa'ee</h6>
                            <ul>
                                <li>The seventh round will end at Marwah.</li>
                                <li>Face the Kaabah and make dua.</li>
                                <li>Offer two rakats of Nafil in the Haram (optional) if the timing is not Makrooh.</li>
                            </ul>
