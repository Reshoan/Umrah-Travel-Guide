                            <h5>Step 2: Ihraam</h5>
                            <p>Ihraam means to enter into a state in which certain conditions apply - it becomes unlawful to use certain halal things. When a person pronounces the Talbiyah (labbaik…), certain halal things become haram upon him.</p>
                            <ul>
                                <li>A pilgrim should trim the nails, shave the armpits and pubic hair, take a bath, comb the hair, and apply perfume before donning Ihraam garments.</li>
                                <li>These prohibitions apply as soon as the pilgrim makes the intention for Hajj or Umrah and starts reciting Talbiyah.</li>
                                <li>The intention for Hajj or Umrah should be made at Meeqat or before.</li>
                                <li>The two Nafils of Ihraam can be prayed at home, at the airport, or on the airplane. It is Sunnah to recite Surah Al-Kafiroon in the first Rakat and Surah Al-Ikhlaas in the second Rakat.</li>
                            </ul>
                                <h6>Ihraam for Men</h6>
                                <p>The two unstitched garments that a male pilgrim wears are also called Ihraam, but the real Ihraam are Niyyah (intention) and Talbiyah. Without the latter, one does not enter the state of Ihraam.</p>
                                <p>Male pilgrims should wear sandals or slippers so that the central bone of the foot is left uncovered.</p>

                                <h6>Ihraam for Women</h6>
                                <P>A woman’s Ihraam consists of her normal clothes that cover her Awrah. Nothing should be seen except her face, hands, and feet. In the presence of non-mahrams, the face should be concealed in a manner that nothing touches the face itself (for those who wear a Niqab).</P>


                                <h6>Prohibited Acts in Ihraam</h6>
                                <ul>
                                    <li>A pilgrim may not apply perfume, use any item that contains fragrance, or consume fragrant raw food.</li>
                                    <li>He must not cut nails, cut, or pluck hair.</li>
                                    <li>Must not marry nor propose.</li>
                                    <li>Must not indulge in intimate talk with sexual undertones or have conjugal relations.</li>
                                    <li>Must not hurt or kill animals.</li>
                                    <li>Must not commit a sin.</li>
                                    <li>Must not get involved in arguments or quarrels.</li>
                                    <li>Must not remove dirt from the body or cause the hair to fall.</li>
                                    <li>Male pilgrims must not wear stitched clothes.</li>
                                    <li>Men must not wear footwear covering the central bone of the foot.</li>
                                    <li>Men must not cover their head and face.</li>
                                    <li>Women must not cover their faces. In the presence of men, they must ensure a covering that does not touch the face.</li>
                                    <li>A pilgrim should not wipe the face with a towel or cloth.</li>
                                </ul>
                                
                                <h6>Permitted Acts in Ihraam</h6>
                                <ul>
                                    <li>To take a bath without rubbing off dirt from the body.</li>
                                    <li>To wear a ring, watch, sunglasses, money belt, hearing aid, etc.</li>
                                    <li>To use an umbrella.</li>
                                    <li>To use a miswaak.</li>
                                    <li>To file a broken nail.</li>
                                    <li>To apply non-fragrant Kajal.</li>
                                    <li>To tie a bandage.</li>
                                    <li>To place head or cheek on the pillow.</li>
                                    <li>To kill a dangerous animal.</li>
                                    <li>To change Ahraam garments.</li>
                                    <li>To use a blanket while sleeping (head and face uncovered).</li>
                                    <li>To pull out a tooth where necessary.</li>
                                </ul>
                                
                            
