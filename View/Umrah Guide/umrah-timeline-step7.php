                        <div class="text-left mx-auto mb-5 wow fadeInUp" data-wow-delay="0.1s" style="max-width: 600px;">
                            <!--<img src="img/step2.png" alt="Step 1" class="img-fluid mb-3">-->
                            <h5>Step 7: Tawaf:</h5>
                            <p>Tawaf involves going around the Kaabah anti-clockwise 7 times, beginning from the Black Stone (Hajr-e-Aswad).</p>

                            <h6>Preparation</h6>
                            <ul>
                                <li>Pass the upper sheet of Ahraam from underneath the right arm and drape it on the left shoulder. This act bares the right shoulder and is called Iditiba.</li>
                                <li>Tawaf must be performed in a state of Wudhu.</li>
                                <li>Recitation of Talbiyah is stopped when you reach The Black Stone.</li>
                            </ul>
                        
                            <h6>Intention (Niyyah)</h6>
                            <p>Intention is an essential part of Tawaf.</p>
                            <p>Stand in front of the Kaabah facing The Black Stone (Hajr-e-Aswad). Then, without raising your hands, make the intention for Umrah:</p>
                            <blockquote>"Oh Allah, I intend to perform Tawaf of Umrah to please You. Accept it from me and make it easy for me."</blockquote>
                        
                            <h6>Istilam</h6>
                            <ul>
                                <li>Go towards Hajr-e-Aswad and kiss it if possible, or touch it with a stick and kiss the stick.</li>
                                <li>If that is not possible, raise your hands to your ears, keeping your palms open towards Hajr-e-Aswad and say:</li>
                            </ul>
                            
                            <p style="font-weight: bold; font-size: 20px;">Bismillahi Allahu Akbar WaLilla Hil Hamd</p>
                            
                            <ul>
                                <li>Drop your hands down.</li>
                                <li>Point your palms again towards Hajr-e-Aswad and kiss them.</li>
                                <li>This act is called Istilam.</li>
                            </ul>
                        
                            <h6>Start of Tawaf</h6>
                            <ul>
                                <li>Start the Tawaf around the Kaabah anti-clockwise.</li>
                                <li>The Hateem (semi-circle) must be encircled during the Tawaf because it is part of the Kaabah.</li>
                                <li>During Tawaf, the person must not face the Kaabah (except when starting Tawaf at the Black Stone).</li>
                            </ul>
                        
                            <h6>Ramal</h6>
                            <ul>
                                <li>For the first three circuits, men are required to walk briskly. This is a Sunnah act called Ramal.</li>
                                <li>In the remaining four circuits, they should walk at a normal pace.</li>
                                <li>If brisk walking is not possible due to heavy crowds, they may walk normally.</li>
                                <li>Ramal and Idtiba are only performed in a Tawaf followed by Sa’ee.</li>
                            </ul>
                        
                            <h6>Supplications During Tawaf</h6>
                            <p>There are no fixed supplications for Tawaf. One can recite the Quran, Names of Allah, do Zikr, and make personal Duas. Duas do not have to be necessarily in Arabic; one can supplicate in any language as long as the heart is engaged.</p>
                            <p>Between the Rukn-e-Yemeni (The Yemeni or the Fourth Corner) and the Black Stone, it is Sunnah to recite the following prayer:</p>
                            <p style="direction: rtl; font-size: 24px; font-weight: bold; text-align: center;">
                                رَبَّنَا آتِنَا فِي الدُّنْيَا حَسَنَةً وَفِي الْآخِرَةِ حَسَنَةً وَقِنَا عَذَابَ النَّارِ❁
                            </p>
                        
                            <p style="font-size: 20px; font-weight: bold;"> Rabbanā ātinā fi d-dunyā ḥasanatan wa fi l-ākhirati ḥasanatan wa qinā ʿadhāba n-nār.</p>
                        
                            
                            <blockquote>"O Allah, grant us the best in this world and in the next world and save us from the torment of the fire."</blockquote>
                        
                            <p>It is Sunnah to touch Rukn-e-Yamani with both hands or with one hand as you pass it. If you cannot due to heavy crowds, pass by without motioning towards it.</p>
                        
                            <h6>Hatim</h6>
                            <p>Hatim is a semi-circular half-built wall that was originally part of the Kaabah but was left out due to lack of funds when the Kaabah was rebuilt by the Quraish. It is obligatory to encircle the Hatim during Tawaf.</p>
                        
                            <h6>End of Tawaf</h6>
                            <ul>
                                <li>When you complete 7 rounds, perform Istilam of Hajr-e-Aswad for the eighth time, saying:</li>
                            </ul>
                            
                            <p style="font-weight: bold; font-size: 20px;">Bismillahi Allahu Akbar Wa Lillah Hil Hamd</p>
                        
                            <p>Your Tawaf is now complete. Cover both shoulders now.</p>
                        
                            <h6>Two Units of Prayer</h6>
                            <ul>
                                <li>Next, offer two units of prayer behind or close to Maqam-e-Ibrahim without covering your head.</li>
                                <li>If it is Makruh time, wait until the time is suitable for Nafil prayer.</li>
                                <li>If there are heavy crowds, the prayer can be offered anywhere in the Haram.</li>
                                <li>It is Sunnah to recite Surah Al-Kafiroon in the first Rakat and Surah Al-Ikhlas in the second Rakat.</li>
                            </ul>
                        
                            <h6>Zamzam</h6>
                            <ul>
                                <li>Now proceed to drink Zamzam water.</li>
                                <li>Face the Kaabah and drink it while standing.</li>
                                <li>Whatever intention is made while drinking Zamzam is fulfilled.</li>
                            </ul>
                        
                            <h6>Etiquette During Tawaf</h6>
                            <ul>
                                <li>Do not indulge in useless or worldly talk.</li>
                                <li>Do not recite or make Duas in a loud voice as it disturbs others.</li>
                                <li>After completing Tawaf, it is preferable to go to the Multazim (wall between the Black Stone and the door of the Kaabah). Cling to this wall and make fervent Duas. Supplications made here are accepted.</li>
                                <li>Women should avoid going near the Kaabah if it is very crowded.</li>
                                <li>Touching the Hajr-e-Aswad or the Multazim is Sunnah, but non-Mahram men touching women is a sin.</li>
                                <li>Women cannot perform Tawaf during menstruation. They are not supposed to bare their shoulders or walk briskly in Tawaf like men.</li>
                                <li>If the ground floor is heavily crowded, they should use the upper floors for Tawaf.</li>
                                <li>During Tawaf, a pilgrim may not face the Kaabah or look at it, except at the starting point.</li>
                                <li>If Wudhu is broken during Tawaf:
                                    <ul>
                                        <li>If it breaks before completing four rounds, the Tawaf must be restarted.</li>
                                        <li>If it breaks after four rounds, the pilgrim may resume from where they left off.</li>
                                    </ul>
                                </li>
                            </ul>
